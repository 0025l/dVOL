     */
    public function store(Request $request)
    {
        $loop = $request->komunitas;
         $id_keg = $request->id_keg;
         foreach ($loop as $value){
            $dataw = new komkeg;
            $dataw->nama_kom = $value;
            $dataw->id_keg = $request->id_keg;
            $dataw->save();
        }
        $data = modelKegiatan::where('id_keg', $id_keg)->get();
    
        return view('konfkegiatan',compact('data'));
    }

